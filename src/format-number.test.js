const { test } = require('@kmamal/testing')
const { formatNumber } = require('./format-number')

test("formatNumber", (t) => {
	t.equal(formatNumber(0), '0')
	t.equal(formatNumber(-1), '-1')
	t.equal(formatNumber(5), '5')
	t.equal(formatNumber(-5), '-5')
	t.equal(formatNumber(100), '100')
	t.equal(formatNumber(-100), '-100')
	t.equal(formatNumber(1000), '1.000')
	t.equal(formatNumber(-1000), '-1.000')
	t.equal(formatNumber(1000000), '1.000.000')
	t.equal(formatNumber(-1000000), '-1.000.000')
	t.equal(formatNumber(0.2), '0,2')
	t.equal(formatNumber(-0.2), '-0,2')
	t.equal(formatNumber(0.002), '0,002')
	t.equal(formatNumber(-0.002), '-0,002')
	t.equal(formatNumber(0.0002), '0,000.2')
	t.equal(formatNumber(-0.0002), '-0,000.2')
	t.equal(formatNumber(0.0000002), '0,000.000.2')
	t.equal(formatNumber(-0.0000002), '-0,000.000.2')
})
